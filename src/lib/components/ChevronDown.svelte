<script lang="ts">
  // Source: https://movingicons.dev

  interface IconProps {
    color?: string;
    size?: number;
    strokeWidth?: number;
    animate?: boolean;
    class?: string;
  }

  let {
    color = "currentColor",
    size = 24,
    strokeWidth = 2,
    animate = false,
    class: className = ""
  }: IconProps = $props();

  function handleMouseEnter() {
    if (animate) return;
    animate = true;
    setTimeout(() => {
      animate = false;
    }, 200);
  }
</script>

<div class={className} aria-label="chevron-down" role="img" onmouseenter={handleMouseEnter}>
  <svg
    xmlns="http://www.w3.org/2000/svg"
    width={size}
    height={size}
    viewBox="0 0 24 24"
    fill="none"
    stroke={color}
    stroke-width={strokeWidth}
    stroke-linecap="round"
    stroke-linejoin="round"
  >
    <path d="m6 9 6 6 6-6" class:chevron={animate} />
  </svg>
</div>

<style>
  div {
    display: inline-block;
  }
  path {
    transition: all 0.2s ease-in;
  }

  .chevron {
    transform: translateY(3px);
  }
</style>
